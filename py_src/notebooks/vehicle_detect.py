def iou_xyxy(a, b):
    ax1, ay1, ax2, ay2 = a
    bx1, by1, bx2, by2 = b
    ix1, iy1 = max(ax1, bx1), max(ay1, by1)
    ix2, iy2 = min(ax2, bx2), min(ay2, by2)
    iw, ih = max(0, ix2 - ix1), max(0, iy2 - iy1)
    inter = iw * ih
    au = max(0, ax2 - ax1) * max(0, ay2 - ay1)
    bu = max(0, bx2 - bx1) * max(0, by2 - by1)
    return inter / (au + bu - inter + 1e-6)

def nms_union(boxes, thr=0.5):
    if not boxes:
        return []

    max_input = int(CFG.get("NMS_MAX_INPUT", 6000))
    if len(boxes) > max_input:
        boxes = sorted(boxes, key=lambda x: float(x[4]), reverse=True)[:max_input]

    try:
        by_cls = {}
        for b in boxes:
            cls_id = _box_cls_id(b)
            by_cls.setdefault(cls_id, []).append(b)

        kept = []
        for cls_boxes in by_cls.values():
            rects = []
            scores = []
            for b in cls_boxes:
                x1, y1, x2, y2, c = b[:5]
                w = max(1, int(x2) - int(x1))
                h = max(1, int(y2) - int(y1))
                rects.append([int(x1), int(y1), w, h])
                scores.append(float(c))

            idxs = cv2.dnn.NMSBoxes(rects, scores, score_threshold=0.0, nms_threshold=float(thr))
            if idxs is None or len(idxs) == 0:
                continue

            for i in np.array(idxs).reshape(-1).tolist():
                kept.append(cls_boxes[int(i)])

        kept.sort(key=lambda x: float(x[4]), reverse=True)
        return kept
    except Exception:
        boxes = sorted(boxes, key=lambda x: x[4], reverse=True)
        kept = []
        for b in boxes:
            drop = False
            for k in kept:
                cls_b = _box_cls_id(b)
                cls_k = _box_cls_id(k)
                if cls_b is not None and cls_k is not None and cls_b != cls_k:
                    continue
                if iou_xyxy(b[:4], k[:4]) > thr:
                    drop = True
                    break
            if not drop:
                kept.append(b)
        return kept

def refine_boxes_by_vote(kept_boxes, source_boxes, vote_iou=0.35):
    if not kept_boxes or not source_boxes:
        return kept_boxes

    out = []
    for kb in kept_boxes:
        kx1, ky1, kx2, ky2, kc = kb[:5]
        ktail = tuple(kb[5:])
        kcls = _box_cls_id(kb)

        sx1 = float(kx1) * float(max(kc, 1e-6))
        sy1 = float(ky1) * float(max(kc, 1e-6))
        sx2 = float(kx2) * float(max(kc, 1e-6))
        sy2 = float(ky2) * float(max(kc, 1e-6))
        sw = float(max(kc, 1e-6))
        best_c = float(kc)

        for sb in source_boxes:
            scls = _box_cls_id(sb)
            if kcls is not None and scls is not None and kcls != scls:
                continue
            if iou_xyxy(kb[:4], sb[:4]) < float(vote_iou):
                continue

            sc = float(sb[4])
            bx1, by1, bx2, by2 = sb[:4]
            w = max(sc, 1e-6)
            sx1 += float(bx1) * w
            sy1 += float(by1) * w
            sx2 += float(bx2) * w
            sy2 += float(by2) * w
            sw += w
            if sc > best_c:
                best_c = sc

        if sw > 0.0:
            rx1 = int(round(sx1 / sw))
            ry1 = int(round(sy1 / sw))
            rx2 = int(round(sx2 / sw))
            ry2 = int(round(sy2 / sw))
        else:
            rx1, ry1, rx2, ry2 = int(kx1), int(ky1), int(kx2), int(ky2)

        out.append((rx1, ry1, rx2, ry2, float(best_c), *ktail))

    out.sort(key=lambda x: float(x[4]), reverse=True)
    return out

def _vk_expand_xyxy(x1, y1, x2, y2, frac, W, H):
    w = max(1.0, float(x2) - float(x1))
    h = max(1.0, float(y2) - float(y1))
    dx = float(frac) * w
    dy = float(frac) * h
    xx1 = max(0.0, float(x1) - dx)
    yy1 = max(0.0, float(y1) - dy)
    xx2 = min(float(W - 1), float(x2) + dx)
    yy2 = min(float(H - 1), float(y2) + dy)
    return (xx1, yy1, xx2, yy2)

def _vk_interval_gap(a1, a2, b1, b2):
    a1 = float(a1); a2 = float(a2); b1 = float(b1); b2 = float(b2)
    if a2 < b1:
        return b1 - a2
    if b2 < a1:
        return a1 - b2
    return 0.0

def _vk_axis_overlap_ratio(a1, a2, b1, b2):
    a1 = float(a1); a2 = float(a2); b1 = float(b1); b2 = float(b2)
    ov = max(0.0, min(a2, b2) - max(a1, b1))
    denom = max(1e-6, min(a2 - a1, b2 - b1))
    return ov / denom

def _vk_merge_parts_union(boxes, W, H,
                          iou_thr=0.25,
                          exp_frac=0.25,
                          exp_iou_thr=0.05,
                          axis_ov_thr=0.60,
                          gap_frac=0.35,
                          center_frac=2.0):
    if not boxes:
        return []

    n = len(boxes)
    parent = list(range(n))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def unite(a, b):
        ra, rb = find(a), find(b)
        if ra != rb:
            parent[rb] = ra

    P = []
    for box in boxes:
        x1, y1, x2, y2, c = box[:5]
        tail = tuple(box[5:])
        x1f = float(x1)
        y1f = float(y1)
        x2f = float(x2)
        y2f = float(y2)
        w = max(1.0, x2f - x1f)
        h = max(1.0, y2f - y1f)
        cx = 0.5 * (x1f + x2f)
        cy = 0.5 * (y1f + y2f)
        P.append((x1f, y1f, x2f, y2f, float(c), w, h, cx, cy, tail))

    for i in range(n):
        x1i, y1i, x2i, y2i, ci, wi, hi, cxi, cyi, tail_i = P[i]
        cls_i = tail_i[0] if len(tail_i) > 0 else None

        for j in range(i + 1, n):
            x1j, y1j, x2j, y2j, cj, wj, hj, cxj, cyj, tail_j = P[j]
            cls_j = tail_j[0] if len(tail_j) > 0 else None

            if cls_i is not None and cls_j is not None and cls_i != cls_j:
                continue
            if abs(cxi - cxj) > float(center_frac) * max(wi, wj):
                continue
            if abs(cyi - cyj) > float(center_frac) * max(hi, hj):
                continue

            if iou_xyxy((x1i, y1i, x2i, y2i), (x1j, y1j, x2j, y2j)) >= float(iou_thr):
                unite(i, j)
                continue

            ei = _vk_expand_xyxy(x1i, y1i, x2i, y2i, exp_frac, W, H)
            ej = _vk_expand_xyxy(x1j, y1j, x2j, y2j, exp_frac, W, H)
            if iou_xyxy(ei, ej) >= float(exp_iou_thr):
                unite(i, j)
                continue

            yov = _vk_axis_overlap_ratio(y1i, y2i, y1j, y2j)
            xov = _vk_axis_overlap_ratio(x1i, x2i, x1j, x2j)
            xgap = _vk_interval_gap(x1i, x2i, x1j, x2j)
            ygap = _vk_interval_gap(y1i, y2i, y1j, y2j)

            if (yov >= float(axis_ov_thr)) and (xgap <= float(gap_frac) * max(wi, wj)):
                unite(i, j)
                continue
            if (xov >= float(axis_ov_thr)) and (ygap <= float(gap_frac) * max(hi, hj)):
                unite(i, j)
                continue

    clusters = {}
    for idx in range(n):
        r = find(idx)
        clusters.setdefault(r, []).append(idx)

    merged = []
    for comp in clusters.values():
        x1 = min(P[k][0] for k in comp)
        y1 = min(P[k][1] for k in comp)
        x2 = max(P[k][2] for k in comp)
        y2 = max(P[k][3] for k in comp)

        best_k = max(comp, key=lambda k: P[k][4])
        c = P[best_k][4]
        tail = P[best_k][9]

        x1 = int(max(0, min(int(round(x1)), W - 1)))
        y1 = int(max(0, min(int(round(y1)), H - 1)))
        x2 = int(max(0, min(int(round(x2)), W - 1)))
        y2 = int(max(0, min(int(round(y2)), H - 1)))

        if x2 > x1 and y2 > y1:
            merged.append((x1, y1, x2, y2, float(c), *tail))

    merged.sort(key=lambda t: t[4], reverse=True)
    return merged

def filter_boxes_geom(boxes, cfg=None):
    if cfg is None:
        return boxes
    out = []
    ar_min = float(cfg.get("ar_min", 0.25))
    ar_max = float(cfg.get("ar_max", 5.0))
    min_side = int(cfg.get("min_side", 4))
    for box in boxes:
        x1, y1, x2, y2, c = box[:5]
        tail = tuple(box[5:])
        w = x2 - x1
        h = y2 - y1
        if w <= 0 or h <= 0:
            continue
        s = min(w, h)
        if s < min_side:
            continue
        ar = w / float(h)
        if ar < ar_min or ar > ar_max:
            continue
        out.append((x1, y1, x2, y2, c, *tail))
    return out

def crop_with_pad(img, x1, y1, x2, y2, pad=0.8):
    H, W = img.shape[:2]
    w = x2 - x1
    h = y2 - y1
    cx = (x1 + x2) / 2
    cy = (y1 + y2) / 2
    nw = w * (1 + pad)
    nh = h * (1 + pad)

    rx1 = int(max(0, cx - nw / 2))
    ry1 = int(max(0, cy - nh / 2))
    rx2 = int(min(W, cx + nw / 2))
    ry2 = int(min(H, cy + nh / 2))
    if rx2 <= rx1 or ry2 <= ry1:
        return None
    return img[ry1:ry2, rx1:rx2]

