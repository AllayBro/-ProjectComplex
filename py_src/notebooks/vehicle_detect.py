def resolve_model_path() -> Path:
    candidates = []

    p = Path(CFG.get("MODEL_PATH", ""))
    if p and p.exists():
        return p

    drone = Path(CFG["DRONE"])
    for rel in ["data_det", "models", "weights", "runs", "."]:
        d = drone / rel
        if d.exists():
            candidates.extend(sorted(d.rglob("*.pt")))

    for d in [Path("/content"), Path("/content/runs"), Path(CFG["RUNS"]), Path(CFG["DET_ROOT"]), Path(CFG["CLS_ROOT"])]:
        if d.exists():
            candidates.extend(sorted(d.rglob("*.pt")))

    uniq = []
    seen = set()
    for c in candidates:
        c = c.resolve()
        if c not in seen:
            uniq.append(c)
            seen.add(c)

    if not uniq:
        raise FileNotFoundError(
            "Не найдено ни одного файла весов *.pt.\n"
            "Проверьте, что в Google Drive реально есть веса, и что DRONE указывает на правильную папку.\n"
            f"DRONE={drone}\n"
            f"Ожидаемые места: {drone/'data_det'} или любой подкаталог DRONE, либо /content."
        )

    if len(uniq) == 1:
        CFG["MODEL_PATH"] = str(uniq[0])
        log(f"auto-selected MODEL_PATH: {CFG['MODEL_PATH']}")
        return uniq[0]

    raise FileNotFoundError(
        "Найдено несколько файлов весов *.pt. "
        "Интерактивный выбор отключён. "
        "Укажите точный путь в CFG['MODEL_PATH'] или передайте model_path через cfg в run()."
    )


