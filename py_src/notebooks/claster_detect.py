def refine_car_with_tiles_whole(image, base_box):
    """
    Уточняет бокс одной машины тайлами, но:
    - работает в ROI вокруг base_box (использует ROI_SCALE / ROI_MIN_SIZE),
    - принимает кандидаты даже без IoU с base_box (склейка по осевому перекрытию + допустимому gap),
    - склеивает части машины не только IoU'ом (иначе перед/зад не объединятся).
    """
    H, W = image.shape[:2]
    x1_b, y1_b, x2_b, y2_b = base_box

    x1_b = max(0.0, min(float(W - 1), x1_b))
    y1_b = max(0.0, min(float(H - 1), y1_b))
    x2_b = max(0.0, min(float(W - 1), x2_b))
    y2_b = max(0.0, min(float(H - 1), y2_b))

    w_b = x2_b - x1_b
    h_b = y2_b - y1_b
    if w_b <= 1 or h_b <= 1:
        return [x1_b, y1_b, x2_b, y2_b]

    cx_b = 0.5 * (x1_b + x2_b)
    cy_b = 0.5 * (y1_b + y2_b)

    # --- ROI вокруг машины ---
    roi_w = max(float(ROI_MIN_SIZE), float(ROI_SCALE) * w_b)
    roi_h = max(float(ROI_MIN_SIZE), float(ROI_SCALE) * h_b)

    rx1 = cx_b - 0.5 * roi_w
    ry1 = cy_b - 0.5 * roi_h
    rx2 = cx_b + 0.5 * roi_w
    ry2 = cy_b + 0.5 * roi_h

    rx1 = int(max(0.0, min(float(W - 1), rx1)))
    ry1 = int(max(0.0, min(float(H - 1), ry1)))
    rx2 = int(max(0.0, min(float(W - 1), rx2)))
    ry2 = int(max(0.0, min(float(H - 1), ry2)))

    if rx2 - rx1 <= 1 or ry2 - ry1 <= 1:
        return [x1_b, y1_b, x2_b, y2_b]

    roi = image[ry1:ry2, rx1:rx2]

    # расширенный базовый бокс (чтобы не отбрасывать “края”)
    base_exp = expand_xyxy([x1_b, y1_b, x2_b, y2_b], 0.25, W, H)

    # лимиты смещения центра (используем Ваши CENTER_SCALE_X/Y)
    dx_lim = 0.5 * float(CENTER_SCALE_X) * w_b
    dy_lim = 0.5 * float(CENTER_SCALE_Y) * h_b

    tiles = tile_image(roi, tile_size=TILE_SIZE, overlap=OVERLAP)

    candidates = []

    # пороги склейки частей (локально, чтобы не ломать остальную программу)
    MERGE_EXP_FRAC = 0.25
    MERGE_EXP_IOU  = 0.05
    MERGE_AXIS_OV  = 0.60
    MERGE_GAP_FRAC = 0.35

    # --- собираем кандидатов из тайлов в ROI ---
    for x0, y0, tile in tiles:
        results = model.predict(
            tile,
            imgsz=TILE_SIZE,
            conf=CONF_THRES,
            verbose=False,
            **_predict_device_kwargs(),
        )
        r = results[0]
        if r.boxes is None or len(r.boxes) == 0:
            continue

        for b in r.boxes:
            cls_id = int(b.cls[0])
            if cls_id not in CAR_CLASSES:
                continue

            x1, y1, x2, y2 = b.xyxy[0].tolist()

            gx1 = rx1 + x0 + x1
            gy1 = ry1 + y0 + y1
            gx2 = rx1 + x0 + x2
            gy2 = ry1 + y0 + y2

            gx1 = max(0.0, min(float(W - 1), gx1))
            gy1 = max(0.0, min(float(H - 1), gy1))
            gx2 = max(0.0, min(float(W - 1), gx2))
            gy2 = max(0.0, min(float(H - 1), gy2))

            if gx2 <= gx1 + 1 or gy2 <= gy1 + 1:
                continue

            cand = [gx1, gy1, gx2, gy2]

            # центр кандидата
            cx_c = 0.5 * (gx1 + gx2)
            cy_c = 0.5 * (gy1 + gy2)

            if abs(cx_c - cx_b) > dx_lim:
                continue
            if abs(cy_c - cy_b) > dy_lim:
                continue

            w_c = gx2 - gx1
            h_c = gy2 - gy1

            # мягкий отсев “мусора”
            if (w_c < 0.20 * w_b) and (h_c < 0.20 * h_b):
                continue
            if (w_c > 3.00 * w_b) or (h_c > 3.00 * h_b):
                continue

            iou_base = bbox_iou(cand, [x1_b, y1_b, x2_b, y2_b])
            iou_exp  = bbox_iou(cand, base_exp)

            # если IoU почти нет, всё равно берём “перед/зад” по осевому правилу
            y_ov = axis_overlap_ratio(cand[1], cand[3], base_exp[1], base_exp[3])
            x_ov = axis_overlap_ratio(cand[0], cand[2], base_exp[0], base_exp[2])

            x_gap = interval_gap(cand[0], cand[2], base_exp[0], base_exp[2])
            y_gap = interval_gap(cand[1], cand[3], base_exp[1], base_exp[3])

            ok = False
            if iou_base >= CAND_IOU_MIN or iou_exp >= max(0.02, 0.5 * CAND_IOU_MIN):
                ok = True
            elif (y_ov >= 0.60) and (x_gap <= 0.40 * max(w_b, w_c)):
                ok = True
            elif (x_ov >= 0.60) and (y_gap <= 0.40 * max(h_b, h_c)):
                ok = True

            if ok:
                candidates.append(cand)

    if not candidates:
        return [x1_b, y1_b, x2_b, y2_b]

    # добавляем базовый бокс как якорь
    candidates.append([x1_b, y1_b, x2_b, y2_b])
    base_idx = len(candidates) - 1

    # --- склейка кандидатов: IoU + IoU расширенных + осевое перекрытие/разрыв ---
    n = len(candidates)
    parent = list(range(n))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(a, b):
        ra, rb = find(a), find(b)
        if ra != rb:
            parent[rb] = ra

    for i in range(n):
        bi = candidates[i]
        wi = max(1.0, bi[2] - bi[0])
        hi = max(1.0, bi[3] - bi[1])
        ei = expand_xyxy(bi, MERGE_EXP_FRAC, W, H)

        for j in range(i + 1, n):
            bj = candidates[j]
            wj = max(1.0, bj[2] - bj[0])
            hj = max(1.0, bj[3] - bj[1])
            ej = expand_xyxy(bj, MERGE_EXP_FRAC, W, H)

            merged = False

            if bbox_iou(bi, bj) >= IOU_THR:
                merged = True
            elif bbox_iou(ei, ej) >= MERGE_EXP_IOU:
                merged = True
            else:
                y_ov = axis_overlap_ratio(bi[1], bi[3], bj[1], bj[3])
                x_ov = axis_overlap_ratio(bi[0], bi[2], bj[0], bj[2])

                x_gap = interval_gap(bi[0], bi[2], bj[0], bj[2])
                y_gap = interval_gap(bi[1], bi[3], bj[1], bj[3])

                if (y_ov >= MERGE_AXIS_OV) and (x_gap <= MERGE_GAP_FRAC * max(wi, wj)):
                    merged = True
                elif (x_ov >= MERGE_AXIS_OV) and (y_gap <= MERGE_GAP_FRAC * max(hi, hj)):
                    merged = True

            if merged:
                union(i, j)

    root_base = find(base_idx)
    group = [candidates[i] for i in range(n) if find(i) == root_base]

    if not group:
        return [x1_b, y1_b, x2_b, y2_b]

    xs1 = [b[0] for b in group]
    ys1 = [b[1] for b in group]
    xs2 = [b[2] for b in group]
    ys2 = [b[3] for b in group]

    x1_new = max(0.0, min(xs1))
    y1_new = max(0.0, min(ys1))
    x2_new = min(float(W - 1), max(xs2))
    y2_new = min(float(H - 1), max(ys2))

    if SHRINK_FRAC > 0.0:
        w_new = max(1.0, x2_new - x1_new)
        h_new = max(1.0, y2_new - y1_new)
        dx = float(SHRINK_FRAC) * w_new
        dy = float(SHRINK_FRAC) * h_new
        x1_new = max(0.0, x1_new + dx)
        y1_new = max(0.0, y1_new + dy)
        x2_new = min(float(W - 1), x2_new - dx)
        y2_new = min(float(H - 1), y2_new - dy)

    return [int(x1_new), int(y1_new), int(x2_new), int(y2_new)]


def process_image():
    fname, img = load_image_from_upload()
    H, W = img.shape[:2]
    print("Файл:", fname)
    print("Размер изображения (W x H):", W, "x", H)

    # 1. Обычная детекция
    full_boxes = detect_cars_full(img)
    print("Найдено машин обычным способом:", len(full_boxes))

    # 2. Если вообще никого не нашли → тайлы по всему кадру
    if not full_boxes:
        print("Обычная детекция не нашла машин, включается тайловый режим по всему кадру.")
        tiled_boxes = detect_cars_on_tiles_whole(img)
        draw_cars(img, tiled_boxes)
        return

    # 3. Если всё изображение помещается в 640×640 → кластеризация НЕ нужна
    if W <= TILE_SIZE and H <= TILE_SIZE:
        draw_cars(img, full_boxes)
        return

    # 4. Основная логика — кластеры только если БОКС машины больше тайла
    final_boxes = []
    clustered_indices = set()

    for idx, b in enumerate(full_boxes):
        x1, y1, x2, y2, conf, cls_id = b
        w = x2 - x1
        h = y2 - y1

        # условие: машина слишком крупная, не помещается в один 640×640
        need_cluster = (w > TILE_SIZE or h > TILE_SIZE)

        if need_cluster:
            # уточняем кластеризацией по всем тайлам кадра
            nx1, ny1, nx2, ny2 = refine_car_with_tiles_whole(img, [x1, y1, x2, y2])
            final_boxes.append([nx1, ny1, nx2, ny2, conf, cls_id])
            clustered_indices.add(idx + 1)
        else:
            # машина помещается в тайл → оставляем как есть
            final_boxes.append(b)

    # 5. Отрисовка + вывод размеров
    draw_cars(img, final_boxes, clustered_indices)
    return

