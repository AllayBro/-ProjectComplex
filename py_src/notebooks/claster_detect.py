def tile_image(image, tile_size=TILE_SIZE, overlap=OVERLAP):
    """Нарезка переданного изображения на тайлы. Для патча меньше 640×640 тайлов нет."""
    h, w = image.shape[:2]

    if h <= tile_size and w <= tile_size:
        return [(0, 0, image)]

    step = int(tile_size * (1.0 - overlap))
    step = max(1, step)

    tiles = []
    for y in range(0, max(h - tile_size, 0) + 1, step):
        for x in range(0, max(w - tile_size, 0) + 1, step):
            x2 = x + tile_size
            y2 = y + tile_size
            tile = image[y:y2, x:x2]
            tiles.append((x, y, tile))

    # добавление правого и нижнего краёв
    if w > tile_size:
        x = w - tile_size
        x = max(0, x)
        for y in range(0, max(h - tile_size, 0) + 1, step):
            x2 = x + tile_size
            y2 = y + tile_size
            tile = image[y:y2, x:x2]
            tiles.append((x, y, tile))

    if h > tile_size:
        y = h - tile_size
        y = max(0, y)
        for x in range(0, max(w - tile_size, 0) + 1, step):
            x2 = x + tile_size
            y2 = y + tile_size
            tile = image[y:y2, x:x2]
            tiles.append((x, y, tile))

    uniq = {}
    for x0, y0, t in tiles:
        uniq[(x0, y0)] = t
    tiles = [(x0, y0, t) for (x0, y0), t in uniq.items()]

    return tiles


def bbox_iou(box_a, box_b):
    ax1, ay1, ax2, ay2 = box_a[:4]
    bx1, by1, bx2, by2 = box_b[:4]

    ix1 = max(ax1, bx1)
    iy1 = max(ay1, by1)
    ix2 = min(ax2, bx2)
    iy2 = min(ay2, by2)

    iw = max(0.0, ix2 - ix1)
    ih = max(0.0, iy2 - iy1)
    inter = iw * ih
    if inter <= 0:
        return 0.0

    area_a = max(0.0, ax2 - ax1) * max(0.0, ay2 - ay1)
    area_b = max(0.0, bx2 - bx1) * max(0.0, by2 - by1)
    union = area_a + area_b - inter
    if union <= 0:
        return 0.0

    return inter / union
def expand_xyxy(box, frac, W, H):
    x1, y1, x2, y2 = box[:4]
    w = max(1.0, x2 - x1)
    h = max(1.0, y2 - y1)
    dx = frac * w
    dy = frac * h
    x1 = max(0.0, x1 - dx)
    y1 = max(0.0, y1 - dy)
    x2 = min(float(W - 1), x2 + dx)
    y2 = min(float(H - 1), y2 + dy)
    return [x1, y1, x2, y2]

def interval_gap(a1, a2, b1, b2):
    if a2 < b1:
        return b1 - a2
    if b2 < a1:
        return a1 - b2
    return 0.0

def axis_overlap_ratio(a1, a2, b1, b2):
    ov = max(0.0, min(a2, b2) - max(a1, b1))
    denom = max(1e-6, min(a2 - a1, b2 - b1))
    return ov / denom



def cluster_boxes_iou(boxes, iou_thr=IOU_THR):
    """Группировка боксов по IoU, одна группа = одна машина (для тайловой обработки)."""
    if not boxes:
        return []

    n = len(boxes)
    parent = list(range(n))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(a, b):
        ra, rb = find(a), find(b)
        if ra != rb:
            parent[rb] = ra

    for i in range(n):
        for j in range(i + 1, n):
            if boxes[i][5] != boxes[j][5]:
                continue
            if bbox_iou(boxes[i], boxes[j]) >= iou_thr:
                union(i, j)

    clusters = {}
    for idx in range(n):
        root = find(idx)
        clusters.setdefault(root, []).append(idx)

    merged = []
    for comp in clusters.values():
        xs1 = [boxes[i][0] for i in comp]
        ys1 = [boxes[i][1] for i in comp]
        xs2 = [boxes[i][2] for i in comp]
        ys2 = [boxes[i][3] for i in comp]
        confs = [boxes[i][4] for i in comp]
        cls_id = boxes[comp[0]][5]

        x1 = min(xs1)
        y1 = min(ys1)
        x2 = max(xs2)
        y2 = max(ys2)
        conf = max(confs)

        merged.append([x1, y1, x2, y2, conf, cls_id])

    return merged


