def bbox_iou(a, b):
    ax1, ay1, ax2, ay2 = a
    bx1, by1, bx2, by2 = b
    ix1 = max(ax1, bx1)
    iy1 = max(ay1, by1)
    ix2 = min(ax2, bx2)
    iy2 = min(ay2, by2)
    iw = max(0.0, ix2 - ix1)
    ih = max(0.0, iy2 - iy1)
    inter = iw * ih
    if inter <= 0:
        return 0.0
    area_a = max(0.0, ax2 - ax1) * max(0.0, ay2 - ay1)
    area_b = max(0.0, bx2 - bx1) * max(0.0, by2 - by1)
    union = area_a + area_b - inter
    if union <= 0:
        return 0.0
    return inter / union

def rotate_image_and_mask(img, mask, angle_deg):
    h, w = img.shape[:2]
    cx, cy = w * 0.5, h * 0.5
    M = cv2.getRotationMatrix2D((cx, cy), angle_deg, 1.0)

    img_rot = cv2.warpAffine(img, M, (w, h), flags=cv2.INTER_LINEAR, borderMode=cv2.BORDER_CONSTANT, borderValue=(0, 0, 0))
    mask_rot = cv2.warpAffine(mask, M, (w, h), flags=cv2.INTER_NEAREST, borderMode=cv2.BORDER_CONSTANT, borderValue=0)

    return img_rot, mask_rot, M
def mask_span_x(mask):
    ys, xs = np.where(mask > 0)
    if xs.size == 0:
        return None
    return float(xs.max() - xs.min() + 1)


def robust_thr_from_mad(values, k):
    values = values[np.isfinite(values)]
    if values.size == 0:
        return None
    med = np.median(values)
    mad = np.median(np.abs(values - med))
    return float(med + k * (mad + 1e-9))

def mode_int(arr):
    arr = np.asarray(arr, dtype=np.int32)
    arr = arr[arr > 0]
    if arr.size == 0:
        return None
    mx = int(arr.max())
    bc = np.bincount(arr, minlength=mx + 1)
    return int(bc.argmax())

def comp_len_near_boundary(edge_row_bool, x_boundary, x0, x1):
    seg = edge_row_bool[x0:x1]
    if seg.size == 0 or not np.any(seg):
        return 0

    idx = np.flatnonzero(seg)
    splits = np.where(np.diff(idx) > 1)[0]
    starts = np.r_[0, splits + 1]
    ends   = np.r_[splits, idx.size - 1]

    best_len = 0
    best_dist = None
    xb = x_boundary - x0

    for s, e in zip(starts, ends):
        run = idx[s:e+1]
        run_len = int(run.size)
        run_center = float(np.mean(run))
        dist = abs(run_center - xb)
        if best_dist is None or dist < best_dist:
            best_dist = dist
            best_len = run_len

    return best_len

