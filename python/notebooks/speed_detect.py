def estimate_meters_per_pixel_from_lane_dashes(img_bgr, bbox, dash_len_m=3.0):
    # Простой оценщик масштаба: ищем жёлтые/оранжевые штрихи разметки и измеряем их длину в пикселях
    # dash_len_m — реальная длина штриха (обычно 3 м), можно поменять в CFG

    H, W = img_bgr.shape[:2]
    x1, y1, x2, y2 = bbox

    # ROI ниже машины (разметка обычно там)
    pad = int(0.5 * max(1, (y2 - y1 + 1)))
    ry1 = min(H - 1, y2)
    ry2 = min(H - 1, y2 + pad)
    rx1 = max(0, x1 - int(0.3 * (x2 - x1 + 1)))
    rx2 = min(W - 1, x2 + int(0.3 * (x2 - x1 + 1)))

    if ry2 <= ry1 or rx2 <= rx1:
        return None

    roi = img_bgr[ry1:ry2+1, rx1:rx2+1]
    hsv = cv2.cvtColor(roi, cv2.COLOR_BGR2HSV)

    # маска “жёлто-оранжевого”
    lower = np.array([10, 70, 70], dtype=np.uint8)
    upper = np.array([40, 255, 255], dtype=np.uint8)
    m = cv2.inRange(hsv, lower, upper)

    m = cv2.medianBlur(m, 5)
    m = cv2.morphologyEx(m, cv2.MORPH_OPEN, np.ones((3,3), np.uint8), iterations=1)

    # контуры штрихов
    cnts, _ = cv2.findContours(m, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if not cnts:
        return None

    lengths_px = []
    for c in cnts:
        area = cv2.contourArea(c)
        if area < 40:
            continue
        rect = cv2.minAreaRect(c)
        (cx, cy), (rw, rh), ang = rect
        L = max(rw, rh)
        S = min(rw, rh)
        if S <= 1:
            continue
        # штрих — вытянутый прямоугольник
        if L / S < 3.0:
            continue
        lengths_px.append(float(L))

    if not lengths_px:
        return None

    dash_px = float(np.median(lengths_px))
    if dash_px <= 1:
        return None

    return float(dash_len_m / dash_px)


def process_image_speed_by_blur():
    fname, pil, img_bgr, exif = load_image_from_upload()
    H, W = img_bgr.shape[:2]

    print("Файл:", fname)
    print("Размер (W x H):", W, "x", H)
    print("EXIF ExposureTime (s):", exif.get("ExposureTime_s"))
    print("EXIF FocalLength (mm):", exif.get("FocalLength_mm"))

    vehicles = detect_vehicle_mask(img_bgr, CFG)
    if not vehicles:
        print("Машины не найдены.")
        return

    results = []

    for idx, v in enumerate(vehicles, start=1):
        mask_full = v["mask"]
        bbox = v["bbox"]
        cls_id = v["cls"]
        conf = v["conf"]

        x1, y1, x2, y2 = bbox

        roi_img = img_bgr[y1:y2+1, x1:x2+1].copy()
        roi_mask = mask_full[y1:y2+1, x1:x2+1].copy()

        gray = cv2.cvtColor(roi_img, cv2.COLOR_BGR2GRAY)

        theta = estimate_motion_direction(gray, roi_mask, CFG)
        if theta is None:
            continue

        roi_img_rot, roi_mask_rot, _ = rotate_image_and_mask(roi_img, roi_mask, -theta)
        gray_rot = cv2.cvtColor(roi_img_rot, cv2.COLOR_BGR2GRAY)

        K_px, k_dbg = estimate_blur_length_k(gray_rot, roi_mask_rot, CFG)
        # выдержка: EXIF -> иначе default
                # выдержка: EXIF -> иначе default
        T_s_used = exif.get("ExposureTime_s")
        if T_s_used is None:
            T_s_used = CFG["default_exposure_s"]

        # уверенность смаза (0..1) -> используем как вес, НЕ как запрет
        bc = blur_confidence(gray_rot, roi_mask_rot, 0.0, K_px)
        bc_floor = float(CFG["blur_conf_floor"])
        bc_pow = float(CFG["blur_conf_pow"])
        bc_w = max(bc_floor, float(bc)) ** bc_pow

        # 1) масштаб по разметке (если найден)
        m_per_px = estimate_meters_per_pixel_from_lane_dashes(
            img_bgr=img_bgr,
            bbox=bbox,
            dash_len_m=CFG["lane_dash_len_m"]
        )

        speed_lane = None
        if (m_per_px is not None) and (K_px is not None) and (float(K_px) > 0.0) and (float(T_s_used) > 0.0):
            v_m_s = (float(K_px) * float(m_per_px)) / float(T_s_used)
            speed_lane = float(v_m_s * 3.6) * bc_w

        # 2) масштаб по типовой длине авто (fallback, как раньше)
        span_px = mask_span_x(roi_mask_rot)
        speed_vehicle = compute_speed_kmh(
            K_px=K_px,
            T_s=T_s_used,
            cls_id=cls_id,
            obj_span_px=span_px,
            cfg=CFG
        )
        if speed_vehicle is not None:
            speed_vehicle = float(speed_vehicle) * bc_w

        # 3) итоговая скорость: если есть оба — смешиваем, иначе берём что есть
        speed_kmh = None
        if (speed_lane is not None) and (speed_vehicle is not None):
            alpha = float(CFG["lane_vs_vehicle_alpha"])
            speed_kmh = alpha * float(speed_lane) + (1.0 - alpha) * float(speed_vehicle)
        elif speed_lane is not None:
            speed_kmh = speed_lane
        elif speed_vehicle is not None:
            speed_kmh = speed_vehicle


        results.append({
            "idx": idx,
            "cls": cls_id,
            "conf": conf,
            "bbox": bbox,
            "theta": theta,
            "K": K_px,
            "T_s": T_s_used,
            "speed": speed_kmh,
        })

    if not results:
        print("Не удалось посчитать параметры ни для одной машины.")
        return

    # Печать результатов по всем
    for r in results:
        print(f"#{r['idx']} cls={r['cls']} conf={r['conf']:.3f} theta={r['theta']:.1f} K={None if r['K'] is None else round(r['K'],2)} T={r['T_s']}")

        if r["speed"] is None:
            print("  speed: None")
        else:
            print("  speed (km/h):", round(r["speed"], 2))

    # Отрисовка всех машин на одном изображении
    draw_results_multi(img_bgr, results)

