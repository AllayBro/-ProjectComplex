# 4) СКОРОСТЬ по формуле v = z * K * sx / (T * f)
#    (как в статье) — нужны z, T, f и sx (или sensor_width)
# -----------------------------
def compute_speed_kmh(
    K_px,
    z_m=None,
    T_s=None,
    focal_mm=None,
    sensor_width_mm=None,
    pixel_size_um=None,
    image_width_px=None,
    *,
    obj_span_px=None,
    cls_id=None,
    cfg=None
):
    # K_px — длина смаза в пикселях (в уже повернутом ROI, где смаз горизонтальный)

    if K_px is None or K_px <= 0:
        return None

    # Режим А (старый): если вдруг есть все параметры — считаем по оптике
    have_optical = (
        (z_m is not None) and (T_s is not None) and (focal_mm is not None) and
        (
            (pixel_size_um is not None and pixel_size_um > 0) or
            (sensor_width_mm is not None and sensor_width_mm > 0 and image_width_px is not None and image_width_px > 0)
        )
    )
    if have_optical:
        if pixel_size_um is not None and pixel_size_um > 0:
            sx_m = (pixel_size_um * 1e-6)
        else:
            sx_m = (sensor_width_mm * 1e-3) / float(image_width_px)

        f_m = (focal_mm * 1e-3)
        v_m_s = float(z_m) * float(K_px) * float(sx_m) / (float(T_s) * float(f_m))
        return float(v_m_s * 3.6)

    # Режим B (новый): без z/f/sensor — по типовой длине ТС и его размеру в пикселях вдоль смаза
    if cfg is None:
        return None

    if T_s is None or T_s <= 0:
        T_s = cfg.get("default_exposure_s")
    if T_s is None or T_s <= 0:
        return None

    if obj_span_px is None or obj_span_px <= 0:
        return None

    lengths = cfg.get("vehicle_length_m_by_cls", {})
    default_len = cfg.get("default_vehicle_length_m")

    L_m = None
    if cls_id is not None:
        L_m = lengths.get(int(cls_id))
    if L_m is None:
        L_m = default_len

    if L_m is None or L_m <= 0:
        return None

    # meters_per_pixel = L_m / obj_span_px
    # v = (K_px * meters_per_pixel) / T_s
    v_m_s = (float(K_px) * (float(L_m) / float(obj_span_px))) / float(T_s)
    return float(v_m_s * 3.6)


