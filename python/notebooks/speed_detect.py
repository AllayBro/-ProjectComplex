def draw_results_multi(img_bgr, results):
    vis = img_bgr.copy()
    H, W = vis.shape[:2]

    for r in results:
        x1, y1, x2, y2 = r["bbox"]
        cv2.rectangle(vis, (x1, y1), (x2, y2), (0, 255, 0), 2)

        cx = int((x1 + x2) * 0.5)
        cy = int((y1 + y2) * 0.5)

        theta = r["theta"]
        if theta is not None:
            L = int(round(0.25 * max(1, (x2 - x1 + 1))))
            ang = np.deg2rad(theta)
            dx = int(round(L * np.cos(ang)))
            dy = int(round(L * np.sin(ang)))
            p1 = (cx - dx, cy - dy)
            p2 = (cx + dx, cy + dy)
            cv2.arrowedLine(vis, p1, p2, (0, 0, 255), 2, tipLength=0.2)

        K_px = r["K"]
        sp = r["speed"]

        lines = [f"#{r['idx']} K={None if K_px is None else round(K_px,1)}px"]
        if sp is not None:
            lines.append(f"v={round(sp,1)} km/h")


        ty = max(15, y1 - 5)
