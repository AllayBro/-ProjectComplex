def _exif_to_float(v):
    if v is None:
        return None
    if isinstance(v, (int, float)):
        return float(v)
    if isinstance(v, tuple) and len(v) == 2:
        num, den = v
        den = float(den)
        if den != 0:
            return float(num) / den
        return None
    try:
        return float(v)
    except Exception:
        return None

def read_exif(pil_img):
    exif = {}
    try:
        raw = pil_img.getexif()
        for tag_id, value in raw.items():
            tag = TAGS.get(tag_id, tag_id)
            exif[tag] = value
    except Exception:
        pass

    out = {
        "ExposureTime_s": None,
        "FocalLength_mm": None,
        "Make": None,
        "Model": None
    }

    out["ExposureTime_s"] = _exif_to_float(exif.get("ExposureTime"))
    out["FocalLength_mm"]  = _exif_to_float(exif.get("FocalLength"))
    out["Make"]  = exif.get("Make")
    out["Model"] = exif.get("Model")
    return out

