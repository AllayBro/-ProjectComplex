def estimate_blur_length_k(gray, mask, cfg):
    gray_f = gray.astype(np.float32)
    sobx = cv2.Sobel(gray_f, cv2.CV_32F, 1, 0, ksize=3)
    edge = np.abs(sobx)

    m = (mask > 0)
    if np.count_nonzero(m) == 0:
        return None, None

    thr = robust_thr_from_mad(edge[m], cfg["edge_thr_k_mad"])
    if thr is None:
        return None, None

    H, W = gray.shape[:2]
    rows_sums = []

    for y in range(H):
        xs = np.flatnonzero(mask[y] > 0)
        if xs.size == 0:
            continue

        xL = int(xs.min())
        xR = int(xs.max())
        obj_w = max(1, xR - xL + 1)

        if xs.size < int(round(cfg["min_row_coverage"] * obj_w)):
            continue

        win = int(round(cfg["edge_win_frac"] * obj_w))
        win = max(3, win)

        x0L = max(0, xL - win)
        x1L = min(W, xL + win + 1)

        x0R = max(0, xR - win)
        x1R = min(W, xR + win + 1)

        row_bool = (edge[y] > thr)

        wL = comp_len_near_boundary(row_bool, xL, x0L, x1L)
        wR = comp_len_near_boundary(row_bool, xR, x0R, x1R)

        s = int(wL + wR)
        if s > 0:
            rows_sums.append(s)

    rows_sums = np.asarray(rows_sums, dtype=np.int32)
    if rows_sums.size == 0:
        return None, None

    s_mode = mode_int(rows_sums)
    if s_mode is None or s_mode <= 0:
        return None, None

    K = 0.5 * float(s_mode)

    hist_trace = [(float(K), int(s_mode), int(rows_sums.size))]

    for _ in range(int(cfg["k_iter_max"])):
        keep_thr = cfg["k_keep_ratio"] * (2.0 * K)
        kept = rows_sums[rows_sums >= keep_thr]

        if kept.size == 0:
            break

        s_mode2 = mode_int(kept)
        if s_mode2 is None or s_mode2 <= 0:
            break

        K2 = 0.5 * float(s_mode2)
        hist_trace.append((float(K2), int(s_mode2), int(kept.size)))

        if abs(K2 - K) <= float(cfg["k_converge_eps"]):
            K = K2
            break

        K = K2

    return float(K), {
        "thr": float(thr),
        "rows_used": int(rows_sums.size),
        "trace": hist_trace
    }

def blur_confidence(gray_rot, mask_rot, theta_deg, K_px):
    # Быстрая проверка: если K маленький или контраст вдоль направления смаза не падает — считаем, что смаза нет.
    if K_px is None or K_px < 3.0:
        return 0.0

    g = gray_rot.astype(np.float32)
    gx = cv2.Sobel(g, cv2.CV_32F, 1, 0, ksize=3)
    gy = cv2.Sobel(g, cv2.CV_32F, 0, 1, ksize=3)

    m = (mask_rot > 0)
    if np.count_nonzero(m) == 0:
        return 0.0

    t = np.deg2rad(theta_deg)
    # производная вдоль смаза (после поворота смаз примерно по X -> t около 0, но оставим универсально)
    d_along = gx * np.cos(t) + gy * np.sin(t)
    d_perp  = -gx * np.sin(t) + gy * np.cos(t)

    a = float(np.mean(np.abs(d_along[m])))
    p = float(np.mean(np.abs(d_perp[m])))

    if p <= 1e-6:
        return 0.0

    # при реальном смазе вдоль смаза градиент меньше, чем поперёк
    ratio = a / p  # меньше => лучше
    conf = max(0.0, min(1.0, (0.9 - ratio) / 0.9))  # ratio<=0.0..0.9 -> 1..0
    return float(conf)

