def detect_vehicle_mask(img_bgr, cfg):
    # Возвращает СПИСОК объектов:
    # [{"mask":mask_uint8(H,W), "bbox":[x1,y1,x2,y2], "cls":cls_id, "conf":conf, "area":area_px}, ...]
    model_path = str(cfg.get("yolo_model_path") or cfg.get("model_name") or "").strip().strip('"').strip("'")
    if not model_path:
        raise RuntimeError("yolo_model_path/model_name не задан.")

    yolo_dir = str(cfg.get("yolo_dir", "") or "").strip().strip('"').strip("'")
    if yolo_dir and (not os.path.isabs(model_path)):
        model_path = os.path.join(yolo_dir, model_path)

    model_path = os.path.abspath(os.path.expandvars(os.path.expanduser(model_path)))
    if not os.path.exists(model_path):
        raise RuntimeError(f"Файл модели YOLO не найден: {model_path}")

    model = YOLO(model_path)
    H, W = img_bgr.shape[:2]

    res = model.predict(
        img_bgr,
        conf=cfg["det_conf"],
        imgsz=cfg.get("imgsz", 640),
        verbose=False
    )[0]

    if res.boxes is None or len(res.boxes) == 0:
        return []

    boxes = res.boxes.xyxy.cpu().numpy().astype(np.float32)
    confs = res.boxes.conf.cpu().numpy().astype(np.float32)
    clss  = res.boxes.cls.cpu().numpy().astype(np.int32)

    keep = [i for i, c in enumerate(clss) if int(c) in CAR_CLASSES]
    if not keep:
        return []

    masks_data = None
    masks_xy = None
    if res.masks is not None:
        if hasattr(res.masks, "data") and res.masks.data is not None:
            masks_data = res.masks.data.cpu().numpy()  # (N, h, w) в размере модели
        if hasattr(res.masks, "xy") and res.masks.xy is not None:
            masks_xy = res.masks.xy

    dets = []
    for i in keep:
        x1, y1, x2, y2 = boxes[i].tolist()
        x1 = float(max(0, min(W - 1, x1)))
        y1 = float(max(0, min(H - 1, y1)))
        x2 = float(max(0, min(W - 1, x2)))
        y2 = float(max(0, min(H - 1, y2)))
        dets.append({
            "i": int(i),
            "cls": int(clss[i]),
            "conf": float(confs[i]),
            "box": [x1, y1, x2, y2]
        })

    if not dets:
        return []

    # --- группировка пересекающихся боксов (склейка частей одной машины) ---
    n = len(dets)
    parent = list(range(n))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(a, b):
        ra, rb = find(a), find(b)
        if ra != rb:
            parent[rb] = ra

    for a in range(n):
        for b in range(a + 1, n):
            if dets[a]["cls"] != dets[b]["cls"]:
                continue
            if bbox_iou(dets[a]["box"], dets[b]["box"]) >= cfg["merge_iou"]:
                union(a, b)

    groups = {}
    for idx in range(n):
        root = find(idx)
        groups.setdefault(root, []).append(idx)

    # --- параметры постобработки маски ---
    min_area = float(cfg.get("min_vehicle_area_frac", 0.0)) * float(H * W)

    k = max(3, int(round(min(H, W) * cfg["mask_dilate_frac"])))
    if k % 2 == 0:
        k += 1
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (k, k))
    close_iter = int(cfg.get("mask_close_iter", 1))

    out = []

    for comp in groups.values():
        cls_id = dets[comp[0]]["cls"]
        conf = max(dets[j]["conf"] for j in comp)

        mask = np.zeros((H, W), dtype=np.uint8)

        if masks_data is not None:
            # ВАЖНО: masks_data[ii] почти всегда НЕ (H,W) -> приводим к (H,W)
            for j in comp:
                ii = dets[j]["i"]
                m = masks_data[ii]  # (h,w) float/0..1

                if (m.shape[0] != H) or (m.shape[1] != W):
                    m = cv2.resize(m.astype(np.float32), (W, H), interpolation=cv2.INTER_NEAREST)

                m = (m > 0.5).astype(np.uint8)
                mask = np.maximum(mask, m * 255)

        elif masks_xy is not None:
            for j in comp:
                ii = dets[j]["i"]
                poly = masks_xy[ii]
                if poly is None:
                    continue
                if isinstance(poly, np.ndarray):
                    poly = [poly]
                for pts in poly:
                    pts_i = np.round(pts).astype(np.int32)
                    if pts_i.size >= 6:
                        cv2.fillPoly(mask, [pts_i], 255)

        else:
            for j in comp:
                x1, y1, x2, y2 = dets[j]["box"]
                x1 = int(max(0, min(W - 1, x1)))
                y1 = int(max(0, min(H - 1, y1)))
                x2 = int(max(0, min(W - 1, x2)))
                y2 = int(max(0, min(H - 1, y2)))
                mask[y1:y2, x1:x2] = 255

        if np.count_nonzero(mask) == 0:
            continue

        # Доработка маски, чтобы захватывало всю машину
        mask = cv2.dilate(mask, kernel, iterations=1)
        mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel, iterations=close_iter)

        area = int(np.count_nonzero(mask))
        if area < min_area:
            continue

        ys, xs = np.where(mask > 0)
        x1 = int(xs.min()); y1 = int(ys.min())
        x2 = int(xs.max()); y2 = int(ys.max())

        out.append({
            "mask": mask,
            "bbox": [x1, y1, x2, y2],
            "cls": cls_id,
            "conf": float(conf),
            "area": area
        })

    out.sort(key=lambda d: d["area"], reverse=True)
    return out


