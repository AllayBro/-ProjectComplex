def estimate_motion_direction(gray, mask, cfg):
    gray_f = gray.astype(np.float32)
    gx = cv2.Sobel(gray_f, cv2.CV_32F, 1, 0, ksize=3)
    gy = cv2.Sobel(gray_f, cv2.CV_32F, 0, 1, ksize=3)

    m = (mask > 0)
    if np.count_nonzero(m) == 0:
        return None

    def energy(theta_deg):
        t = np.deg2rad(theta_deg)
        d = gx * np.cos(t) + gy * np.sin(t)
        return float(np.mean(np.abs(d[m])))

    thetas_coarse = np.arange(cfg["theta_search_min"], cfg["theta_search_max"] + 1e-9, cfg["theta_coarse_step"], dtype=np.float32)
    e_coarse = [(th, energy(float(th))) for th in thetas_coarse]
    best_th, best_e = min(e_coarse, key=lambda x: x[1])

    w = float(cfg["theta_fine_window"])
    thetas_fine = np.arange(best_th - w, best_th + w + 1e-9, cfg["theta_fine_step"], dtype=np.float32)
    thetas_fine = thetas_fine[(thetas_fine >= cfg["theta_search_min"]) & (thetas_fine <= cfg["theta_search_max"])]

    e_fine = [(th, energy(float(th))) for th in thetas_fine]
    best_th2, best_e2 = min(e_fine, key=lambda x: x[1])

    return float(best_th2)

