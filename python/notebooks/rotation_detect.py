def _estimate_deskew_angle_deg(img_bgr: np.ndarray, max_side: int = 960):
    """
    Оценка доминирующего угла линий в кадре (градусы в диапазоне [-90, 90)).
    Возвращает None, если линий мало/нет.
    """
    if img_bgr is None or img_bgr.size == 0:
        return None
    H, W = img_bgr.shape[:2]
    if H < 32 or W < 32:
        return None

    scale = 1.0
    work = img_bgr
    if max(H, W) > max_side:
        scale = max_side / float(max(H, W))
        work = cv2.resize(work, (max(2, int(W * scale)), max(2, int(H * scale))), interpolation=cv2.INTER_AREA)

    g = cv2.cvtColor(work, cv2.COLOR_BGR2GRAY)
    g = cv2.GaussianBlur(g, (5, 5), 0)

    edges = cv2.Canny(g, 50, 150)
    m = max(work.shape[:2])
    min_len = int(0.10 * m)
    lines = cv2.HoughLinesP(edges, 1, np.pi / 180.0, threshold=80, minLineLength=min_len, maxLineGap=20)
    if lines is None:
        return None

    hist = np.zeros(180, dtype=np.float32)
    for x1, y1, x2, y2 in lines.reshape(-1, 4):
        dx = float(x2 - x1)
        dy = float(y2 - y1)
        L = math.hypot(dx, dy)
        if L < 1.0:
            continue
        ang = math.degrees(math.atan2(dy, dx))
        # нормализация в [-90, 90)
        while ang >= 90.0:
            ang -= 180.0
        while ang < -90.0:
            ang += 180.0
        idx = int(round(ang + 90.0))
        idx = 0 if idx < 0 else (179 if idx > 179 else idx)
        hist[idx] += L

    if hist.max() <= 0:
        return None

    peak = int(hist.argmax())
    return float(peak - 90.0)

def _affine_to_3x3(M2x3: np.ndarray) -> np.ndarray:
    M2x3 = np.asarray(M2x3, dtype=np.float32).reshape(2, 3)
    return np.vstack([M2x3, np.array([[0.0, 0.0, 1.0]], dtype=np.float32)])

def _affine_compose(M2: np.ndarray, M1: np.ndarray) -> np.ndarray:
    """
    Композиция аффинных преобразований:
    base -> (M1) -> mid -> (M2) -> out
    Возвращает M_total: base -> out
    """
    A = _affine_to_3x3(M2)
    B = _affine_to_3x3(M1)
    C = A @ B
    return C[:2, :].astype(np.float32)

def _looks_upside_down(img_bgr: np.ndarray, frac: float = 0.22, ratio_thr: float = 1.18) -> bool:
    """
    Очень грубая эвристика для 180°:
    если низ кадра заметно ярче верха, часто это “вверх ногами”.
    """
    if img_bgr is None or img_bgr.size == 0:
        return False
    h, w = img_bgr.shape[:2]
    if h < 64 or w < 64:
        return False
    k = max(8, int(h * float(frac)))
    y = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2YCrCb)[:, :, 0].astype(np.float32)
    top = float(np.mean(y[:k, :]))
    bot = float(np.mean(y[h - k:, :]))
    if not (np.isfinite(top) and np.isfinite(bot)):
        return False
    return bot > top * float(ratio_thr)

