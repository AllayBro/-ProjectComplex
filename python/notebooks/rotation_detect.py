def _run_yolo_cars_tiled(im_bgr, conf, iou, imgsz, max_det, tta, tile, overlap):
    H, W = im_bgr.shape[:2]
    if H < 2 or W < 2:
        return []

    t = int(tile)
    ov = float(overlap)
    step = max(1, int(round(t * (1.0 - ov))))

    out = []
    y = 0
    while y < H:
        x = 0
        y2 = min(y + t, H)
        while x < W:
            x2 = min(x + t, W)
            crop = im_bgr[y:y2, x:x2]
            boxes = _run_yolo_cars(crop, conf=conf, iou=iou, imgsz=imgsz, max_det=max_det, tta=tta)
            for (x1, y1, x3, y3, c) in boxes:
                out.append((int(x1 + x), int(y1 + y), int(x3 + x), int(y3 + y), float(c)))
            x += step
        y += step
    return out

def _detect_on_image(im_bgr, conf, iou, imgsz, max_det, tta, tiling, tile, overlap):
    H, W = im_bgr.shape[:2]
    if tiling == "always":
        use_tiling = True
    elif tiling == "auto":
        use_tiling = (max(H, W) > int(tile))
    else:
        use_tiling = False

    if use_tiling:
        return _run_yolo_cars_tiled(im_bgr, conf, iou, imgsz, max_det, tta, tile, overlap)
    return _run_yolo_cars(im_bgr, conf, iou, imgsz, max_det, tta=tta)

def detect_all_cars(
    img_bgr,
    conf=None,
    iou=None,
    imgsz=None,
    max_det=None,
    do_containment=None,
    tiling=None,
    tile=None,
    overlap=None,
    k90=None,
    use_upright=None,
    upright_thr_deg=None,
    merge_iou=None,
    tta=None
):
    if img_bgr is None or img_bgr.size == 0:
        return []

    cfg = DET_CFG
    conf = cfg["conf"] if conf is None else float(conf)
    iou = cfg["iou"] if iou is None else float(iou)
    imgsz = cfg["imgsz"] if imgsz is None else int(imgsz)
    max_det = cfg["max_det"] if max_det is None else int(max_det)

    tiling = cfg["tiling"] if tiling is None else str(tiling)
    tile = cfg["tile"] if tile is None else int(tile)
    overlap = cfg["overlap"] if overlap is None else float(overlap)

    k90 = cfg["k90"] if k90 is None else tuple(int(x) for x in k90)
    use_upright = cfg["use_upright"] if use_upright is None else bool(use_upright)
    upright_thr_deg = cfg["upright_thr_deg"] if upright_thr_deg is None else float(upright_thr_deg)

    merge_iou = cfg["merge_iou"] if merge_iou is None else float(merge_iou)
    do_containment = cfg["do_containment"] if do_containment is None else bool(do_containment)
    tta = cfg["tta"] if tta is None else bool(tta)

    H0, W0 = img_bgr.shape[:2]
    all_boxes = []

    # 0/90/180/270
    for k in k90:
        kk = int(k) % 4
        im = rotate_k90_bgr(img_bgr, kk)
        boxes_rot = _detect_on_image(im, conf, iou, imgsz, max_det, tta, tiling, tile, overlap)
        boxes_base = rotate_boxes_k90_back(boxes_rot, W0, H0, kk)
        all_boxes.extend(boxes_base)

    # произвольный угол (оценка по линиям)
    if use_upright:
        rot = float(estimate_upright_rotation_deg(img_bgr))
        if abs(rot) >= upright_thr_deg:
            imr, M, _ = _rotate_bound(img_bgr, rot)
            boxes_r = _detect_on_image(imr, conf, iou, imgsz, max_det, tta, tiling, tile, overlap)
            boxes_base = _map_boxes_from_rotated_to_base(boxes_r, M, W0, H0)
            all_boxes.extend(boxes_base)

    if all_boxes:
        all_boxes = nms_union(all_boxes, thr=merge_iou)
        if do_containment:
            all_boxes = _containment_suppression(all_boxes, contain_thr=0.92, conf_margin=0.02)

    return all_boxes

