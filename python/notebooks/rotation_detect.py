def rotate_bound(img_bgr: np.ndarray, angle_deg: float):
    """
    Поворот без обрезания краёв (expand). Возвращает:
    rotated_bgr, M3 (3x3), Minv (3x3)
    """
    h, w = img_bgr.shape[:2]
    cx, cy = w / 2.0, h / 2.0

    M = cv2.getRotationMatrix2D((cx, cy), angle_deg, 1.0)  # 2x3
    cos = abs(M[0, 0])
    sin = abs(M[0, 1])

    new_w = int(h * sin + w * cos)
    new_h = int(h * cos + w * sin)

    M[0, 2] += new_w / 2.0 - cx
    M[1, 2] += new_h / 2.0 - cy

    rotated = cv2.warpAffine(img_bgr, M, (new_w, new_h), flags=cv2.INTER_LINEAR)

    M3 = np.array([[M[0, 0], M[0, 1], M[0, 2]],
                   [M[1, 0], M[1, 1], M[1, 2]],
                   [0.0,     0.0,     1.0]], dtype=np.float64)
    Minv = np.linalg.inv(M3)
    return rotated, M3, Minv

def estimate_true_image_rotation_deg(img_bgr: np.ndarray) -> float:
    """
    Оценка непрерывного угла наклона сцены (любой градус: 1, 33, 52.6, 359.9).
    Возвращает угол поворота (в градусах), который нужно ПРИМЕНИТЬ к изображению,
    чтобы доминирующие линии сцены стали ближе к горизонтали/вертикали.
    """
    gray = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2GRAY)

    h, w = gray.shape[:2]
    max_side = max(h, w)
    scale = 1.0
    if max_side > 1400:
        scale = 1400.0 / max_side
        gray = cv2.resize(gray, (int(w * scale), int(h * scale)), interpolation=cv2.INTER_AREA)

    lsd = cv2.createLineSegmentDetector(cv2.LSD_REFINE_STD)
    lines = lsd.detect(gray)[0]

    if lines is None or len(lines) < 12:
        edges = cv2.Canny(gray, 60, 180)
        hough = cv2.HoughLinesP(edges, 1, np.pi / 180.0, threshold=80,
                                minLineLength=60, maxLineGap=10)
        if hough is None or len(hough) < 12:
            return 0.0
        lines = hough.astype(np.float32).reshape(-1, 1, 4)

    angles = []
    weights = []

    for x1, y1, x2, y2 in lines.reshape(-1, 4):
        dx = float(x2 - x1)
        dy = float(y2 - y1)
        length = math.hypot(dx, dy)
        if length < 45.0:
            continue
        ang = math.degrees(math.atan2(dy, dx))  # [-180..180]
        ang = ang % 180.0                       # [0..180)
        angles.append(ang)
        weights.append(length)

    if len(angles) < 12:
        return 0.0

    a = np.deg2rad(np.array(angles, dtype=np.float64))
    wts = np.array(weights, dtype=np.float64)

    c = np.cos(2.0 * a)
    s = np.sin(2.0 * a)
    mean2 = math.atan2(float(np.sum(s * wts)), float(np.sum(c * wts)))
    dom = 0.5 * mean2
    dom_deg = (math.degrees(dom)) % 180.0

    # Поворачиваем к ближайшей оси (0 или 90), но угол остаётся непрерывным
    target = 90.0 if abs(dom_deg - 90.0) < abs(dom_deg - 0.0) else 0.0
    rot = target - dom_deg

    return rot % 360.0

def normalize_scene_single_pass(img_bgr: np.ndarray):
    """
    ЕДИНСТВЕННЫЙ проход нормализации сцены:
    - вычисляет непрерывный угол
    - строит R_img (выровненное изображение)
    Возвращает: R_img, rot_deg, M3, Minv
    """
    rot_deg = estimate_true_image_rotation_deg(img_bgr)
    R_img, M3, Minv = rotate_bound(img_bgr, rot_deg)
    return R_img, rot_deg, M3, Minv

def iou_xyxy(a, b):
    ax1, ay1, ax2, ay2 = a; bx1, by1, bx2, by2 = b
    ix1, iy1 = max(ax1,bx1), max(ay1,by1)
    ix2, iy2 = min(ax2,bx2), min(ay2,by2)
    iw, ih = max(0, ix2-ix1), max(0, iy2-iy1)
    inter = iw*ih
    au = max(0,ax2-ax1)*max(0,ay2-ay1)
    bu = max(0,bx2-bx1)*max(0,by2-by1)
    return inter / (au + bu - inter + 1e-6)

def nms_union(boxes, thr=0.55):
    boxes = sorted(boxes, key=lambda x: x[4], reverse=True)
    kept=[]
    for b in boxes:
        if all(iou_xyxy(b[:4], k[:4]) <= thr for k in kept):
            kept.append(b)
    return kept

def merge_close_small(boxes, max_dist=6, max_size=26):
    """
    Склейка пачек очень мелких боксов, которые
    сидят почти в одной точке (типичная россыпь шума вдали).
    """
    small = []
    big = []
    for b in boxes:
        x1, y1, x2, y2, c = b
        w = x2 - x1
        h = y2 - y1
        if min(w, h) <= max_size:
            small.append(b)
        else:
            big.append(b)

    used = [False] * len(small)
    merged_small = []

    for i, b in enumerate(small):
        if used[i]:
            continue
        x1, y1, x2, y2, c = b
        cx = 0.5 * (x1 + x2)
        cy = 0.5 * (y1 + y2)

        group = [b]
        used[i] = True

        for j in range(i + 1, len(small)):
            if used[j]:
                continue
            x1b, y1b, x2b, y2b, cb = small[j]
            cxb = 0.5 * (x1b + x2b)
            cyb = 0.5 * (y1b + y2b)

            if abs(cxb - cx) <= max_dist and abs(cyb - cy) <= max_dist:
                group.append(small[j])
                used[j] = True

        xs1 = [g[0] for g in group]
        ys1 = [g[1] for g in group]
        xs2 = [g[2] for g in group]
        ys2 = [g[3] for g in group]
        conf = max(g[4] for g in group)

        merged_small.append((
            min(xs1),
            min(ys1),
            max(xs2),
            max(ys2),
            conf
        ))

    return big + merged_small


def filter_boxes_geom(boxes):
    """
    Отсев откровенного мусора по геометрии и уверенности
    без отрезания дальних машин по расстоянию.
    """
    filtered = []

    for (x1, y1, x2, y2, c) in boxes:
        w = x2 - x1
        h = y2 - y1
        if w <= 0 or h <= 0:
            continue

        s = min(w, h)
        ar = w / float(h)

        # совсем микроскопический бокс – ни ориентацию, ни расстояние не посчитать
        if s < 5:
            continue

        # слишком вытянутые: столбы, барьеры, части разметки
        if ar < 0.25 or ar > 5.0:
            continue

        # адаптивный порог по размеру:
        # чем меньше бокс, тем выше требование к conf
        if s < 10 and c < 0.25:
            continue
        if 10 <= s < 20 and c < 0.15:
            continue
        if s >= 20 and c < 0.08:
            continue

        filtered.append((x1, y1, x2, y2, c))

    # склейка россыпи мелких дублей в дальней зоне
    filtered = merge_close_small(filtered)

    return filtered

