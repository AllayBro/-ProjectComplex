SCALE_TINY_MAX = 0.02  # Максимальная относительная высота для tiny-объектов (2% от высоты кадра)
SCALE_SMALL_MAX = 0.05  # Максимальная относительная высота для small-объектов (5% от высоты кадра)
MIN_BOX_TINY = 10  # Минимальный размер бокса (пиксели) для tiny-объектов
MIN_BOX_SMALL = 20  # Минимальный размер бокса для small-объектов
MIN_BOX_NORMAL = 30  # Минимальный размер бокса для нормальных/крупных объектов
CONF_TINY = 0.18  # Требуемая уверенность для tiny-боксов
CONF_SMALL = 0.22  # Требуемая уверенность для small-боксов
CONF_NORMAL = 0.28  # Требуемая уверенность для нормальных боксов

