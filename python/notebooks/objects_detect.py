def get_exif_focal_length(pil_img):  # Получение фокусного расстояния из EXIF
    try:
        exif_data = pil_img._getexif()  # Сырые EXIF-данные
        if exif_data is None:  # Если EXIF нет
            return FOCAL_MM  # Возвращаем значение по умолчанию
        for tag_id, value in exif_data.items():  # Перебор всех тегов EXIF
            tag = ExifTags.TAGS.get(tag_id, tag_id)  # Преобразование ID тега в имя
            if tag == 'FocalLength':  # Ищем тег фокусного расстояния
                return value[0] / value[1] if isinstance(value, tuple) else float(value)  # Преобразуем к float
        return FOCAL_MM  # Если нужный тег не найден
    except Exception:
        return FOCAL_MM  # При ошибке возвращаем значение по умолчанию

def estimate_focal_pixels(focal_mm, sensor_width_mm, image_width_px):  # Перевод фокусного из мм в пиксели
    return (focal_mm / sensor_width_mm) * image_width_px  # Формула: f_pix = f_mm / sensor_width * width_px

def estimate_brightness(image_bgr):  # Оценка средней яркости кадра
    gray = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2GRAY)  # Перевод в градации серого
    return round(float(np.mean(gray)), 2)  # Среднее значение яркости по всем пикселям

